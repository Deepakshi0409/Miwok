package com.example.android.miwok;

public class fam {private String mDefaultTranslation;

    private String mMikowTransalation;

    public fam(String defaultTranslation, String miwokTranslation) {
        mDefaultTranslation = defaultTranslation;
        mMikowTransalation = miwokTranslation;
    }
    public String getDefaultTranslation() {
        return mDefaultTranslation;
    }

    public String getmiwokTranslation() {
        return mMikowTransalation;
    }
}
